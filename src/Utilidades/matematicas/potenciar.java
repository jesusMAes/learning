package Utilidades.matematicas;


public class potenciar {
    
    public double potencia(int x, int y){
        int a;
        int b;
        double resultado;
        a=x;
        b=y;
        resultado=Math.pow(a, b);
        return resultado;
    }
    public double potencia(double x, double y){
        double a;
        double b;
        double resultado;
        a=x;
        b=y;
        resultado=Math.pow(a, b);
        return resultado;
    }
    

}
